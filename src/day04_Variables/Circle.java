package day04_Variables;

public class Circle {

    public static void main(String[] args) {

        // area, perimeter
        double PI = 3.14 ;

        double radius = 5.5 ;
        double diameter = radius * 2; // 10*2 // finds the diameter by multiplying the radius to 2
        double area = PI * radius * radius ; // 10 * 10 * 3.14 //finds the area of the circle
        double perimeter = 2 * PI * radius  ; // 10 * 2 * 3.14 // finds of perimeter of the circle

        System.out.println("perimeter = " + perimeter);
        System.out.println("area = " + area);
        System.out.println("diameter = " + diameter);
        System.out.println("radius = " + radius);





    }





}




