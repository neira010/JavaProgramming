package day04_Variables;

public class Currencies {

    public static void main(String[] args) {

        int dollar = 1000;


        double lira = dollar * 9.53 ;
        double euro = dollar * 0.86 ;
        //jpy, pound, peso, cad, riyal rub,...
        double jpy = dollar * 0.0088 ;
        double pound = dollar * 1.37 ;
        double peso = dollar * 0.048 ;
        double cad = dollar * 0.81 ;
        double riyal = dollar * 0.27 ;
        double rub = dollar * 0.014 ;
        double bam = dollar * 0.59 ;

        System.out.println("lira = " + lira);
        System.out.println("euro = " + euro);
        System.out.println("jpy = " + jpy);
        System.out.println("pound = " + pound);
        System.out.println("peso = " + peso);
        System.out.println("cad = " + cad);
        System.out.println("riyal = " + riyal);
        System.out.println("rub = " + rub);
        System.out.println("bam = " + bam);





    }
}
