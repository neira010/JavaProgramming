public class IsAlphabet {
    public static void main(String[] args) {

        char letter = ';';
        if ((letter>='a' && letter<='z') || (letter>='A' && letter<='Z')){
            System.out.println("Your letter is in alphabet");
        }else{
            System.out.println("Your letter is not in alphabet");
        }











    }
}
